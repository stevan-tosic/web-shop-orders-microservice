<?php declare(strict_types = 1);

namespace App\Core\Domain\Entity\Article;

/**
 * Class ArticleType
 */
class ArticleType
{
    /** @var int */
    private $id;

    /** @var string */
    private $short;

    /** @var string */
    private $title;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getShort(): string
    {
        return $this->short;
    }

    /**
     * @param string $short
     */
    public function setShort(string $short): void
    {
        $this->short = $short;
    }

    /**
     * @return string
     */
    public function getTitle(): string
    {
        return $this->title;
    }

    /**
     * @param string $title
     */
    public function setTitle(string $title): void
    {
        $this->title = $title;
    }

}
